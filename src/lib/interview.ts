export function buildInterviewText(answers: string[]): string {
  const questions = [
    "Расскажите о вашем продукте или услуге так, как будто объясняете другу за чашкой кофе. Что это такое и для кого?",
    'Представьте вашего идеального клиента. Что у него "болит" или какая задача перед ним стоит в тот момент, когда он начинает искать что-то похожее на ваше предложение?',
    "Что происходит сразу после того, как клиент воспользовался вашей услугой? Какой самый первый, самый быстрый результат он получает? Что он чувствует в этот момент?",
    "Почему клиенты выбирают именно вас, а не делают это сами или уходят к другим? Может, у вас проще, быстрее, надежнее или просто удобнее?",
  ];

  return questions
    .map((q, i) => `Вопрос ${i + 1}: ${q}\nОтвет: ${answers[i] || ""}`)
    .join("\n\n");
}
